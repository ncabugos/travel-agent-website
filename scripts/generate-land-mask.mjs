/**
 * One-time generator: rasterizes a Natural Earth land outline (GeoJSON) into a
 * compact, bit-packed equirectangular land mask consumed by components/auth/
 * DotGlobe.tsx (the 'continents' variant). Run with:
 *
 *   node scripts/generate-land-mask.mjs
 *
 * Input  : /tmp/ne_land.geojson  (Natural Earth ne_110m_land, public domain)
 * Output : components/auth/land-mask.ts
 *
 * The mask is row-major (top row = +90° lat, left col = -180° lon), one bit per
 * cell, base64-encoded. A set bit means "this cell center is on land".
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const COLS = 360 // 1° longitude resolution
const ROWS = 180 // 1° latitude resolution

const geo = JSON.parse(readFileSync('/tmp/ne_land.geojson', 'utf8'))

// Collect every polygon as an array of rings ([ [ [lon,lat], ... ], ...hole ]).
const polygons = []
for (const f of geo.features) {
  const g = f.geometry
  if (!g) continue
  if (g.type === 'Polygon') polygons.push(g.coordinates)
  else if (g.type === 'MultiPolygon') for (const p of g.coordinates) polygons.push(p)
}

// Ray-casting point-in-polygon with even-odd rule across all rings of a polygon
// (so interior holes correctly subtract). Returns true if (lon,lat) is inside.
function inPolygon(lon, lat, rings) {
  let inside = false
  for (const ring of rings) {
    for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
      const xi = ring[i][0], yi = ring[i][1]
      const xj = ring[j][0], yj = ring[j][1]
      const intersects = (yi > lat) !== (yj > lat) &&
        lon < ((xj - xi) * (lat - yi)) / (yj - yi) + xi
      if (intersects) inside = !inside
    }
  }
  return inside
}

const total = COLS * ROWS
const bytes = new Uint8Array(Math.ceil(total / 8))
let landCount = 0

for (let row = 0; row < ROWS; row++) {
  // Cell center latitude: row 0 = near +90, increasing row = decreasing lat
  const lat = 90 - (row + 0.5) * (180 / ROWS)
  for (let col = 0; col < COLS; col++) {
    const lon = -180 + (col + 0.5) * (360 / COLS)
    let land = false
    for (const rings of polygons) {
      if (inPolygon(lon, lat, rings)) { land = true; break }
    }
    if (land) {
      const idx = row * COLS + col
      bytes[idx >> 3] |= 1 << (idx & 7)
      landCount++
    }
  }
}

const b64 = Buffer.from(bytes).toString('base64')
const out = `// AUTO-GENERATED by scripts/generate-land-mask.mjs — do not edit by hand.
// Equirectangular land mask, ${COLS}×${ROWS} (1° cells), row-major, top row = +90°.
// Bit set = cell center is on land. Source: Natural Earth 110m land (public domain).
export const LAND_MASK_COLS = ${COLS}
export const LAND_MASK_ROWS = ${ROWS}
export const LAND_MASK_B64 =
  '${b64}'
`

const outPath = resolve(__dirname, '../components/auth/land-mask.ts')
writeFileSync(outPath, out)
console.log(`Wrote ${outPath}`)
console.log(`Land cells: ${landCount}/${total} (${((landCount / total) * 100).toFixed(1)}%), base64 length ${b64.length}`)
